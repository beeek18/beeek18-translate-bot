def transliterate(text: str) -> str:
    """Транслитерировать строку с кириллицы на латиницу по Приказу МИД № 2113."""
    result = []
    for char in text:
        result.append(TRANSLIT_TABLE.get(char, char))
    return "".join(result)


def is_cyrillic_fio(text: str) -> bool:
    """Проверить, что строка содержит только кириллицу, пробелы и дефисы."""
    allowed = set(" -")
    for char in text:
        if char not in allowed and not (
            "\u0400" <= char <= "\u04ff"  # кириллический диапазон Unicode
        ):
            return False
    return bool(text.strip())
